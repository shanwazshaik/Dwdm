# Create a vector with three numbers
numbers <- c(5, 10, 15)

# Basic operations
sum_result <- sum(numbers)       # Sum of the three numbers
mean_result <- mean(numbers)     # Mean of the three numbers
product_result <- prod(numbers)  # Product of the three numbers

# Accessing specific elements
first_number <- numbers[1]       # Access the first number (5)
second_number <- numbers[2]      # Access the second number (10)
third_number <- numbers[3]       # Access the third number (15)

# Finding the maximum and minimum
max_value <- max(numbers)        # Maximum value (15)
min_value <- min(numbers)        # Minimum value (5)